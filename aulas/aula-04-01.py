'''
1 - Var de controle
2 - Condição de parada
3 - att da var de controle
'''
'''% é divisão
i % 2 == 0: isso verifica se o resto da divisão é zero
''' 

# 1 Var de controle
i = 0
# Condição de parada
while i < 10:
    if i % 2 ==0:
        print(f'i = {i}')
    # 3 atualização da variavel de controle
    i += 1 # incremento